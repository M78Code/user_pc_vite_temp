                    <span class="mr-4 text-009" v-if="items.matchType == 2">{{'[' + i18n_t("bet.bowls") + ']'}}</span>
                    <span class="text-a1a text-flow-none font400">{{ items.playName }}</span>
                </div>
                <div class="w-100 text-8a8 fon12 font400">{{ items.home }} <span class="mx-4">v</span> {{ items.away }}
                </div>
            </div>


            <div class="fw-e-s bet-right" v-if="BetViewDataClass.bet_order_status == 1">
                <div class="f-c-c bet-money">
                    <div class="show_img" v-if="items.red_green" >
                        <img v-if="items.red_green == 'green_down'" :src="`${LOCAL_PROJECT_FILE_PREFIX}/image/image/icon_up.png`" alt=""/>
                        <img v-else :src="`${LOCAL_PROJECT_FILE_PREFIX}/image/image/icon_down.png`" alt=""/>
                    </div>
                    <span class="font14 font700 mr-10" :class="{'red_up':items.red_green == 'green_down','green_down':items.red_green == 'red_up'}">
                        {{ compute_value_by_cur_odd_type(items.odds,'','',items.sportId) }}
                    </span>
                    <BetInput :items="items" />
                </div>
                <div class="font12 h12 mt-4">
                    <span class="font400 mr-4 text-8a8">Highest Win</span>
                    <span class="text-1a1 font500"> {{ mathJs.subtract(mathJs.multiply(BetData.bet_amount,items.oddFinally), BetData.bet_amount) || '0.00' }} </span>
                </div>
            </div>

            <div class="fw-e-s bet-right" v-else>
                <div class="f-c-c bet-odds">
                    <span class="font14 font700 mr-10">{{ compute_value_by_cur_odd_type(items.odds,'','',items.sportId) }}</span>
                </div>
                <!-- <BetResult :items="items" /> -->
            </div>

            <div class="bet-delete" v-if="BetViewDataClass.bet_order_status == 1" @click="set_delete">
                <span class="icon-delete"></span>
            </div>

            <div class="bet-delete bet-icon" v-else>
                <!-- 投注确认中 -->
                <img v-if="BetViewDataClass.bet_order_status == 2" :src="`${LOCAL_PROJECT_FILE_PREFIX}/image/png/video/animal.png`" alt=""/>
                <!-- 投注失败 -->
                <img v-if="BetViewDataClass.bet_order_status == 3" :src="`${LOCAL_PROJECT_FILE_PREFIX}/image/png/video/animal.png`" alt=""/>
                <!-- 投注成功 -->
                <img v-if="BetViewDataClass.bet_order_status == 4" :src="`${LOCAL_PROJECT_FILE_PREFIX}/image/png/video/animal.png`" alt=""/>
            </div>
           
        </div>
        <ul class="bet-bet-money f-b-c" v-show="ref_data.show_money">
            <li class="bet-money-li f-c-c font14" @click="set_bet_money(obj)"  v-for="(obj, index) in ref_data.money_list" :key="obj" :class="!(ref_data.max_money < obj && index != 'max') ? '' : 'disabled'">
                {{index == 'max' ? '' : '+' }} {{ obj }}
            </li>
        </ul>
    </div>
</template>

<script setup>

import { onMounted, onUnmounted, reactive } from "vue"
import {LOCAL_PROJECT_FILE_PREFIX,compute_value_by_cur_odd_type,useMittOn,MITT_TYPES,useMittEmit } from "src/core/"
import BetData from 'src/core/bet/class/bet-data-class.js'
import BetViewDataClass from 'src/core/bet/class/bet-view-data-class.js'
import mathJs from 'src/core/bet/common/mathjs.js'

import BetInput from "./bet-input.vue"  // 投注输入框

const props = defineProps({
    items:{},
    index:{}
})


const ref_data = reactive({
    show_money: false, // 显示快捷金额
    max_money: 0, // 最大限额
})

onMounted(()=>{
    useMittOn(MITT_TYPES.EMIT_SHOW_QUICK_AMOUNT, set_show_quick_money).on
})

onUnmounted(()=>{
    useMittOn(MITT_TYPES.EMIT_SHOW_QUICK_AMOUNT, set_show_quick_money).off
})

// 快捷金额 显示隐藏
const set_show_quick_money = (obj = {}) => {
    ref_data.show_money = obj.show
    obj.money_list.max = 'MAX'
    ref_data.money_list = obj.money_list
    ref_data.max_money = obj.max_money
}

// 快捷金额
const set_bet_money = obj => {
    // 获取当前投注金额
    let money = BetData.bet_amount
    let money_ = obj
    // 设置最大投注金额
    if(obj == "MAX"){
        money_ = ref_data.max_money
    }
    // 计算投注金额
    let money_amount = mathJs.add(money,money_)
    // 投注金额 不能大于最大投注金额
    if(money_amount > ref_data.max_money){
        BetData.set_bet_amount(ref_data.max_money)
    }else{
        BetData.set_bet_amount(mathJs.add(money,money_))
    }
    useMittEmit(MITT_TYPES.EMIT_SET_QUICK_AMOUNT)
}

const set_delete = () => {
    // document.getElementsByClassName("bet-list")[0].style.display = "none"
    // BetData.set_bet_state_show(!BetData.bet_state_show)
    BetData.delete_bet_info(props.index)
}

</script>

