<script>
// #TODO VUEX
// import { mapGetters, mapActions, mapMutations } from "vuex";
import GlobalAccessConfig  from  "src/core/access-config/access-config.js"
import v_s_odd_item from "project_path/pages/virtual/virtual_sports_part/virtual_sports_odd_item.vue"
import v_s_match_timer from "project_path/pages/virtual/virtual_sports_part/virtual_sports_match_timer.vue"
import odd_column_item from "project_path/pages/match-list/components/odd_column_item.vue"

// #TODO MIXINS
// import betting from 'project_path/mixins/betting/betting.js';
// import virtual_sports_m_item_mixin from 'project_path/mixins/virtual_sports/virtual_sports_m_item_mixin.js'
import {  PageSourceData  } from "src/core/index.js";
import {MenuData,compute_img_url } from "src/core/index.js";
import lodash from "lodash";
import { useMittOn, useMittEmit, MITT_TYPES } from  "src/core/mitt"
import { reactive, computed, onMounted, onUnmounted, toRefs, watch } from "vue";
export default defineComponent({
  name: "virtual_sports_match_item",
  // #TODO MIXINS
  // mixins:[common],

  components:{
    "v-s-odd-item":v_s_odd_item,
    'v-s-match-timer':v_s_match_timer,
    "odd-column-item":odd_column_item
  },

  props:{
    i:Number,
    match_item:Object,
    match_selected_i:Number,
    other_status:Number,
  },


  setup(props, evnet) {
    const { match_item, match_selected_i, other_status } = toRefs(props);
    const data = reactive({
      // 事件集合
      emitters: [],
      curr_match_map_time:{},
      vsports:null,
      standard_odd_status:0,
      is_basketball_score:false,
    })
    // #TODO EMIT
      // created(){
      //   useMittOn(MITT_TYPES.EMIT_VIDEO_PROCESS_DATA_GOT,VirtualVideo.get_match_video_process);
      // },


      // #TODO VUEX
      // ...mapActions([
      //     // 设置玩法项默认选中
      //     "set_details_item",
      //   ]),
      // ...mapMutations(['set_current_gotodetail_match','set_toast']),
    onMounted(() => {
      // #TODO EMIT
      // useMittOn(MITT_TYPES.EMIT_PRE_COUNTING_EDN,pre_counting_end_handle)
      VirtualVideo.get_match_video_process()
      // #TODO EMIT
      // useMittOn(MITT_TYPES.EMIT_XU_NI_TY_STANDARD_ODD_STATUS,xu_ni_ty_standard_odd_status);

      emitters = [
        useMittOn(MITT_TYPES.EMIT_VIDEO_PROCESS_DATA_GOT, VirtualVideo.get_match_video_process).off,
        useMittOn(MITT_TYPES.EMIT_PRE_COUNTING_EDN, pre_counting_end_handle).off,
        useMittOn(MITT_TYPES.EMIT_XU_NI_TY_STANDARD_ODD_STATUS, xu_ni_ty_standard_odd_status).off,
      ]
    })

    /**
     * 篮球早盘倒计时结束显示列表比分
     */
    const pre_counting_end_handle = () => {
      is_basketball_score = true;
    };
    /**
     * 获取hl的hs
     * @param {Number} hp_i
     * @return Undefined Undefined
     */
    const get_hl_hs = (hp_i) => {
      let hs = 0;
      if(hp_i && hp_i.hl && hp_i.hl[0]){
        hs = hp_i.hl[0].hs;
      }
      return hs;
    };
    /**
     * 获取ol的length
     * @param {Number} hp_i
     * @param {Number} hp_i_i
     * @return Undefined Undefined
     */
    const get_ol_length = (hp_i,hp_i_i) => {
      let ol_list = [];
      if(hp_i && hp_i.hl && hp_i.hl[0] && hp_i.hl[0].ol){
        ol_list = hp_i.hl[0].ol;
      }
      else{
        if([1,4].includes(+match_item.csid)){
          if(match_item.hps && match_item.hps[hp_i_i]){
            if(match_item.hps[hp_i_i].hpid == 1){
              return 3;
            }
          }
        }
      }
      return ol_list.length;
    };
    /**
     * 获取hp指定部分
     * @param {Number} type 0 第一部分; 1 第二部分
     * @return Undefined Undefined
     */
    const get_hp_list = (type) => {
      if(type == 0){
        let hps = [];
        if(match_item && match_item.hps){
          hps = match_item.hps.slice(0,3);
        }
        return hps;
      }
      else if(type == 1){
        let hps = [];
        if(match_item && match_item.hps && match_item.hps.length > 3){
          hps = match_item.hps.slice(3,6);
        }
        return hps;
      }
    };
    /**
     * 滑动赔率容器
     * @param {Number} type 0 第一部分; 1 第二部分
     * @return Undefined Undefined
     */
    const odd_wrapper_pan = ({ direction,isFinal }) => {
      if (get_hp_list(1).length && !isFinal) {
        if(direction == "left"){
          standard_odd_status = 1;
        }
        else{
          standard_odd_status = 0;
        }
        $emit('odd_pan',standard_odd_status);
      }
    };
    /**
     * 获取播放按钮图标路径
     */
    const get_play_btn_class = (match,i) => {
      if(match.match_status == 1){
        if(match_selected_i == i){
          return 'img-playing';
        }
        else{
          return 'img-play';
        }
      }
      else{
        return 'img-disabled';
      }
    };
    /**
     * 切换赛事
     * @param {Number} i 赛事下标
     * @return {Undefined}
     */
    const switch_match_handle = (i,m) => {
      //虚拟篮球不允许切换
      if(m.match_status == 2 || get_curr_sub_menu_type == 1004) return;
      $emit('switch_match',i);
    };
    /**
     * 获取投注项
     * @param {Object} match 赛事
     * @return {Object}
     */
    const get_hl_item = (match) => {

      let hp_item = {}
      let hp_id_convert;
      //足球
      if(match.csid == 1001){
        if(footer_sub_menu_id == 1){      //独赢
          hp_id_convert = 20001
        }
        else if(footer_sub_menu_id == 4){  //让盘
          hp_id_convert = 20004
        }
        else if(footer_sub_menu_id == 2){   // 大小
          hp_id_convert = 20002
        }
        else if(footer_sub_menu_id == 114){  // 大小
          hp_id_convert = 20001   //角球独赢
        }
      }
      //篮球
      else if(match.csid == 1004){
        if(footer_sub_menu_id == 1){      //独赢
          hp_id_convert = 20043
        }
        else if(footer_sub_menu_id == 4){  //让盘
          hp_id_convert = 20045
        }
        else if(footer_sub_menu_id == 2){   // 大小
          hp_id_convert = 20044
        }
      }

      if(match.hps && match.hps.length){
        hp_item = match.hps.filter(hp => hp.hpid == hp_id_convert)[0];
      }
      return hp_item;
    };
    /**
     * 获取赔率列表
     * @param {Object} match 赛事
     * @return {Array}
     */
    const get_ol_list = (hp_item) => {
      let ol_list = [];
      if(hp_item && hp_item.hl && hp_item.hl.length){
        ol_list = hp_item.hl[0].ol;
      }
      return ol_list;
    };
    /**
     * 获取赔率列表
     * @param {Object} match 赛事
     * @return {Array}
     */
    const get_ol_list_f_match = (match) => {
      let ol_list = [];

      let hp_id_convert;
      //足球
      if(match.csid == 1001){
        ol_list = [
          {
            invalid:1
          },
          {
            invalid:1
          },
          {
            invalid:1
          }
        ];

        if(footer_sub_menu_id == 1){      //独赢
          hp_id_convert = 20001
        }
        else if(footer_sub_menu_id == 4){  //让盘
          hp_id_convert = 20004
        }
        else if(footer_sub_menu_id == 2){   // 大小
          hp_id_convert = 20002
        }
        else if(footer_sub_menu_id == 114){  // 大小
          hp_id_convert = null   //角球独赢
          ol_list = [
            {
              invalid:1
            },
            {
              invalid:1
            }
          ];
        }
      }
      //篮球
      else if(match.csid == 1004){
        ol_list = [
          {
            invalid:1
          },
          {
            invalid:1
          },
          {
            invalid:1
          }
        ];

        if(footer_sub_menu_id == 1){      //独赢
          hp_id_convert = 20043
        }
        else if(footer_sub_menu_id == 4){  //让分
          hp_id_convert = 20045
        }
        else if(footer_sub_menu_id == 2){   // 总分大小
          hp_id_convert = 20044
        }
        else if(footer_sub_menu_id == 114){  // 大小
          hp_id_convert = 20043
          // hp_id_convert = null   //角球独赢
          // ol_list = [
          //   {
          //     invalid:1
          //   },
          //   {
          //     invalid:1
          //   }
          // ];
        }

      }

      if(match.hps && match.hps.length && footer_sub_menu_id != 114){
        let hp_item = match.hps.filter(hp => hp.hpid == hp_id_convert)[0];
        if(hp_item && hp_item.hl && hp_item.hl.length){
          if(footer_sub_menu_id == 1){ //独赢
            let ol_l_list = hp_item.hl[0].ol;
            let m = ol_l_list.filter(ol_item => ol_item.ot == "1")[0];
            let he = ol_l_list.filter(ol_item => ol_item.ot == "X")[0];
            let a = ol_l_list.filter(ol_item => ol_item.ot == "2")[0];
            ol_list = [];
            if(m){
              ol_list.push(m);
            }
            if(he){
              ol_list.push(he);
            }
            if(a){
              ol_list.push(a);
            }
          }
          else{ //非独赢
            ol_list = hp_item.hl[0].ol;
          }
        }
      }
      return ol_list;
    };
    /**
     * 跳转至虚拟体育详情
     * @param {Object} match 赛事
     * @return {String}
     */
    const goto_details =(match) => {
      let mid = match.mid;
      let copied = lodash.cloneDeep(match);
      set_current_gotodetail_match(copied);
      set_details_item(0);
      $router.push({
        name: 'virtual_sports_details',
        query: {
          mid: mid,
          i: id,
          home: $route.query.home ? $route.query.home : 'match'
        }
      })
    };
    const xu_ni_ty_standard_odd_status = (status) => {
      standard_odd_status = status;
    };
    /**
     *@description 虚拟体育点击列表小方块投注
     *@param {Object} match_item 最外层赛事数据
     *@param {Object} ol_item 里层ol数据
     *@return {Undefined} undefined
     */
    const item_click4 = (match_item,ol_item) => {
      //对应没有赔率值或者欧赔小于101000,或者虚拟体育赛事状态不为0
      if (match_item.mhs != 0 || !ol_item.ov || ol_item.ov < 101000 || match_item.match_status){
        return;
      }

      let hl_item = get_hl_item(match_item)
      if (!hl_item) return;
      ol_item.id_ = lodash.get(hl_item,'hl[0].hn') ?
        `${match_item.mid}_${hl_item.chpid || hl_item.hpid}_${ol_item.ot}_${hl_item.hl[0].hn}` : ol_item.oid;
      set_bet_obj_config(match_item, hl_item, ol_item);
    }

    // #TODO VUEX GETEERES
    // computed:{
    // ...mapGetters({
    //   footer_sub_menu_id:"get_footer_sub_menu_id",
    //   get_video_process_data:"get_video_process_data",
    //   get_newer_standard_edition:"get_newer_standard_edition",
    //   get_n_s_changed_loaded:"get_n_s_changed_loaded",
    //   get_curr_sub_menu_type:"get_curr_sub_menu_type",
    //   get_theme:'get_theme',
    //   get_access_config,
    // }),
    const footer_sub_menu_id = computed(() => {
      return ""
    });
    const get_video_process_data = computed(() => {
      return ""
    });
    const get_newer_standard_edition = computed(() => {
      return PageSourceData.get_newer_standard_edition();
    });
    const get_curr_sub_menu_type = computed(() => {
      return MenuData.get_curr_sub_menu_type();
    });
    const get_theme = computed(() => {
      return ""
    });
    /**
     * @Description 获取全局配置开关
     * @param {undefined} undefined
     */
     const GlobalAccessConfig = computed(() => {
    });
    const show_debugger_line = computed(() => {
      let wsl = sessionStorage.getItem('wsl');
      if(wsl == '9999') return true;
      return false;
    });
    const show_basketball_score = computed(() => {
      let flag = false;
      if(is_basketball_score){
        flag = true;
      }
      if(get_curr_sub_menu_type == 1004){
        if(match_item.mmp == "INGAME"){
          flag = true;
        }
      }
      return flag;
    })
    watch(
      () => props.other_status,
      (N) => {
        standard_odd_status = n;
      }
    );
    onUnmounted(() => {
      if(vsports){
        vsports.destroy();
      }
      // #TODO EMIT
      emitters.map((x) => x())
    });
    return {
      ...toRefs(data),
      lodash,
      get_newer_standard_edition,
      footer_sub_menu_id,
      get_video_process_data,
      get_newer_standard_edition,
      get_n_s_changed_loaded,
      get_curr_sub_menu_type,
      get_theme,
      get_access_config,
      pre_counting_end_handle,
      get_hl_hs,
      get_ol_length,
      get_hp_list,
      odd_wrapper_pan,
      get_play_btn_class,
      switch_match_handle,
      get_hl_item,
      get_ol_list,
      get_ol_list_f_match,
      goto_details,
      xu_ni_ty_standard_odd_status,
      item_click4,
      show_debugger_line,
      show_basketball_score,
      compute_img_url
    }
  }
})
</script>

